              <h2 id="Purpose">Purpose</h2>
              <br>
              <p>Why are you buying a keyboard? Where are you going to use it? What are you going to use this keyboard for? How much are you willing to pay for it? Knowing what you want and need in a keyboard makes sure that the keyboard you need is what you want. Every component of the mechanical keyboards can be customised, so do not worry about limitations.</p>
