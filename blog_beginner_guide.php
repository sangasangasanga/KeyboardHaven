              <h2 id="Switches">Switches</h2>
              <br>
              <img class="fakeimg" src="img/blogs/blueswitch.png" alt=""/>
              <p class="pt-2">How would you like your keyboard to feel? Let‚Äôs start with the main reason why mechanical keyboards feels better to type on: the switches. How it sounds, how it feels when the keys are pressed, all depends on the kind of switch in your keyboard. The good news is, there are so many type of switches in the market, means you can afford to be nit picky with how you want your keys to feel when you type. To start with, I‚Äôll introduce the most popular type of switches: MX Cherry switches. Most of the mechanical keyboards come with Cherry MX Switches, so all you have to worry about is choosing the type of switches that you might be comfortable with:</p>
              <img class="fakeimg" src="img/blogs/cherries.png" alt=""/>
              <h3 class="pt-3">X-Axis</h3>
              <p>Linear: when you press on the key, it‚Äôs a smooth ride all the way. Ideal for fast typing and gaming.</p>
              <p>Tactile: When you press on the key, you will feel a ‚Äúbump‚Äù Good for people who likes some feedback when typing for accuracy.</p>
              <p>Clicky: When you press on the key, there is a very distinct ‚Äúclick‚Äù sound</p>
              <h3>Y-Axis</h3>
              <p>Soft: Lower actuation force; less force needed to bottom out the key</p>
              <p>Medium: more force needed to bottom out the key. Good for people who feels that their keyboard is ‚Äútoo soft‚Äù</p>
              <br>
              <p>The type of Cherry Switches correspond with the color of the switch. For example the switch that is linear and soft is called the Cherry MX Red Switch.</p>
              <br>
              <h2 id="Design">Design</h2>
              <br>
              <h3>Layouts</h3>
              <p>When choosing for a keyboard, some of the companies offer different sizes of keyboards, with the size dependent on the number of keys on the keyboard. Some of the more popular sizes are the Full-Size (100%), Tenkeyless (87%. 80% of the total keys), 60% and so on. </p>
              <h3>Full Size Keyboards</h3>
              <img class="fakeimg" src="img/blogs/FULL.jpg" alt=""/>
              <p>The Full size keyboard contains all the keys on the standard keyboard. This layout includes the number pad, so this layout is ideal for people who has to work with numbers frequently.</p>
              <h3>Tenkeyless (87%, 80%)</h3>
              <img class="fakeimg" src="img/blogs/VkfP1lH.jpg" alt=""/>
              <p class="pt-2">The 87%, 80% (of the total keys) gets rid of the number pad. Some may prefer this layout because it saves a lot of space on the desk, and it is usually cheaper than their full size counterparts.</p>
              <h3>1800-Compact</h3>
              <img class="fakeimg" src="img/blogs/blue.png" alt=""/>
              <p class="pt-2">For those who wants the number pad, and also the compatibility of Tenkeyless layout, you can look into the 1800-compact layout. However, this layout is a rarity and may not be readily available.</p>
              <h3>60%</h3>
              <img class="fakeimg" src="img/blogs/60.jpg" alt=""/>
              <p class="pt-2">This layout is for the minimalists. This no frills layout keeps only what is essential for everyday typing. Some may find this bare and too less, but there has been a lot of appeal for this layout, some citing the amount of space this keyboard leaves for the rest of the desk, as well as having all the keys within reach of the fingertips (with the help of shortcuts). For the hardcore minimalist, I have another surprise for you‚Ä¶</p>
              <h3>Binary</h3>
              <img class="fakeimg" src="img/blogs/binarykeeb.jpg" alt=""/>
              <p class="pt-2">Not for the faint hearted, this binary keyboard tests your skills in converting words into binary. Everything you need is there, with only 3 keys. Kidding, this one of a kind keyboard is developed by Seen Murphy.</p>
